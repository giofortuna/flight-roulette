import fs from 'fs';
import path from 'path';
import readline from 'readline';
import { fileURLToPath } from 'url';
import { parseCSVLine } from './csv-utils.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const CSV  = path.join(__dirname, '..', 'data', 'raw', 'countries.csv');
const OUT  = path.join(__dirname, '..', 'src', 'country-names.ts');


async function main() {
  if (!fs.existsSync(CSV)) {
    console.error(`Missing source file.\nPlace countries.csv in data/raw/`);
    process.exit(1);
  }

  const rl = readline.createInterface({ input: fs.createReadStream(CSV), crlfDelay: Infinity });
  const entries = [];
  let header = true;

  for await (const line of rl) {
    if (header) { header = false; continue; }
    const fields = parseCSVLine(line);
    const code = fields[1]?.trim();
    const name = fields[2]?.trim();
    if (code && name) entries.push([code, name]);
  }

  entries.sort((a, b) => a[0].localeCompare(b[0]));
  const lines = entries.map(([code, name]) => `  '${code}': '${name.replace(/'/g, "\\'")}',`);
  const out = `// Auto-generated by scripts/build-country-names.js — do not edit manually\nexport const COUNTRY_NAMES: Record<string, string> = {\n${lines.join('\n')}\n};\n`;
  fs.writeFileSync(OUT, out, 'utf8');
  console.log(`Wrote ${entries.length} countries to ${OUT}`);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  main().catch(err => { console.error(err); process.exit(1); });
}
